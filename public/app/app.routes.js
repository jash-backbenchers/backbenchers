angular.module('appRoutes',['ngRoute'])

.config(function($routeProvider,$locationProvider) {
	$routeProvider
	.when('/home',{
		templateUrl:'public/app/views/home.html',
		authenticated:false
	})
	.when('/visituser',{
		templateUrl:'public/app/views/visituser.html',
		authenticated:false
	})
	.when('/visituser/:username',{
		templateUrl:'public/app/views/visituser.html',
		authenticated:false
	})
	.when('/reloaduser',{
		templateUrl:'public/app/views/login.html',
		authenticated:true
	})
	.when('/login',{
		templateUrl:'public/app/views/login.html',
		authenticated:false
	})
	.when('/signup',{
		templateUrl:'public/app/views/signup.html',
		authenticated:false
	})
	.when('/logout',{
		templateUrl:'public/app/views/landingpage.html',
		authenticated:false
	})
	.when('/',{
		templateUrl:'public/app/views/landingpage.html',
		authenticated:false
	})
	.when('/contacts',{
		templateUrl:'public/app/views/contact.html',
		authenticated:true
	})
	.when('/test',{
		templateUrl:'public/app/views/test.html',
		authenticated:false 
	})
	.when('/lib',{
		templateUrl:'public/app/views/lib.html',
		authenticated:false 
	})
	.when('/chat',{
		templateUrl:'public/app/views/chat.html',
		authenticated:true
	})
	.when('/pdfview',{
		templateUrl:'public/app/views/pdfview.html',
		authenticated:true
	})
	.when('/upload',{
		templateUrl:'public/app/views/upload.html',
		authenticated:true
	})
	.when('/mybag',{
		templateUrl:'public/app/views/mybag.html',
		authenticated:true
	})
	.when('/user',{
		templateUrl:'public/app/views/user.html',
		authenticated:true
	})
	.when('/edit',{
		templateUrl:'public/app/views/edit.html',
		authenticated:true
	})
	.when('/preview',{
		templateUrl:'public/app/views/preview.html',
		authenticated:true
	})
	.when('/preview/:id',{
		templateUrl:'public/app/views/preview.html',
		authenticated:true
	})
	.otherwise({
		redirectTo:'/'
	})
	$locationProvider.html5Mode(true);
})

